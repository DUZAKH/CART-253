"use strict";

/**
 * sets up a canvas that is 640x480
*/
function setup() {
    createCanvas(640, 480);
}

/**
 * makes a blue background, makes a wide big rectangle in it
*/
function draw() {
    background(20, 100, 500);
    //the blue background
    rect(100, 50, 440, 380);
    //the wide rectangle
}
